#7-1 jmu-python-简单四则运算 (10 分)
'''
本题要求编写程序，计算2个正整数的和、差、积、商并输出。题目保证输入和输出全部在整型范围内。

输入格式:
输入2个正整数A和B。每行输入一个数据。

输出格式:
在4行中按照格式“A 运算符 B = 结果”顺序输出和、差、积、商。

输入样例:
8
3
输出样例:
8 + 3 = 11
8 - 3 = 5
8 * 3 = 24
8 / 3 = 2
'''
A=int(input())
B=int(input())
print("%d + %d = %d"%(A,B,A+B))
print("%d - %d = %d"%(A,B,A-B))
print("%d * %d = %d"%(A,B,A*B))
print("%d / %d = %d"%(A,B,A/B))
